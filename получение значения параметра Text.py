# Импортируем все классы и функции из модуля tkinter (основной модуль для GUI)
from tkinter import *
# Импортируем модуль ttk (тематические виджеты - более современный стиль)
from tkinter import ttk

# Создаем главное окно приложения
# root - это основное окно, в котором будут располагаться все виджеты
root = Tk()

# Устанавливаем размер окна: ширина 250 пикселей, высота 150 пикселей
root.geometry("250x150")

# Пытаемся установить иконку для окна
try:
    # iconbitmap() - метод для установки иконки окна
    # default="icon.ico" - указываем файл иконки (должен быть в папке с программой)
    root.iconbitmap(default="icon.ico")
except:
    # pass - ничего не делаем, если иконка не найдена (программа продолжает работу)
    pass  # если иконки нет - ничего не делаем

# Создаем кнопку из модуля ttk (тематическая кнопка)
# btn - переменная, которая хранит ссылку на созданный объект кнопки
btn = ttk.Button()

# Размещаем кнопку в окне с помощью метода pack()
# pack() - автоматически располагает виджет в окне (по умолчанию сверху по центру)
btn.pack()

# Устанавливаем текст на кнопке через квадратные скобки (как в словаре)
# btn["text"] - обращаемся к параметру "text" кнопки и присваиваем ему значение "Send"
btn["text"] = "Send"

# Получаем значение параметра "text" из кнопки и сохраняем в переменную btnText
# btn["text"] - читаем текущее значение текста на кнопке
btnText = btn["text"]

# Выводим значение переменной btnText в консоль (терминал)
# В консоли появится: Send
print(btnText)

# Запускаем главный цикл обработки событий окна
# mainloop() - держит окно открытым, обрабатывает действия пользователя (клики, ввод и т.д.)
# Без этого вызова окно сразу закроется после выполнения кода
root.mainloop()